// ES6 Arrow Function
const add = (a, b) => {
  return a + b;
};

console.log(add(5, 3));

// One-line Arrow Function

const add2 = (a, b) => a + b;

console.log(add2(10, 20)); 

// Single Parameter

const square = x => x * x;

console.log(square(5)); 

// Arrow Function with Array

const numbers = [1, 2, 3, 4];

const double = numbers.map(num => num * 2);

console.log(double);  






